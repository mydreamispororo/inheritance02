package override.domain;

public class Company {
	//로그인 메소드()
	public void login() {
		System.out.println("코리아아이티 아카데미에 로그인 합니다.");
	}
	
	//로그아웃 메소드()
	public void logout() {
		System.out.println("코리아아이티 아카데미에서 로그아웃 합니다.");
	}
	
	//turnOn()
	public void turnOn() {
		System.out.println("컴퓨터 전원을 켭니다.");
	}
	
	//turnOff()
	public void turnOff() {
		System.out.println("컴퓨터 전원을 끕니다.");
	}

}
